"""
                                    Manipulando Texto

- Vamos entender como trabalharmos com cadeias de caracteres quando temos uma frase.
"""

frase = "curso em video python"

"""
*  Fatiamento : No python o fatiamento e bem simples 
"""

# Vamos pegar a posição de uma letra
print(frase[9])

# Vamos pegar agora uma sequencia de uma posição a outra
print(frase[9:13])

# Vamos pegar agora de uma certa posição ate a ultima setando uma posição nao existente
print(frase[9:21])